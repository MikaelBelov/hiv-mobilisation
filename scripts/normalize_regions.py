def normalize_region(name):
    """Normalize region name. Returns None if the region should be excluded."""
    if name is None:
        return None
    name = ' '.join(name.split())  # normalize whitespace/newlines
    if name in REGION_MAP:
        return REGION_MAP[name]
    return name


